    private void btncalcularreportesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btncalcularreportesActionPerformed
        Producto prod = (Producto)cboproducto.getSelectedItem();
        Controller controlador = new Controller();
        double total = controlador.totalNoGanadoPorProdOfertado(prod.getIdProducto());
        lbltotal.setText(String.valueOf(total));
        
        DefaultTableModel tabla = new DefaultTableModel();
        tabla.setColumnIdentifiers(new Object[]{" TOTAL "});
        tabla.addRow(new Object[] {total});
        
        tbltotal.setModel(tabla);
        
        int cantidad = controlador.CantidadArticulosOfertadosMas5Dias();
        lblcantidad.setText(String.valueOf(cantidad));
        
    }//GEN-LAST:event_btncalcularreportesActionPerformed

