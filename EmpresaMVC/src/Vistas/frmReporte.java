    private void cargarTabla()
    {
        ProductoController gestor = new ProductoController();
        ArrayList<DTOReporte> reporte = gestor.cantidadVtasPorProducto();
        DefaultTableModel tablareporte = new DefaultTableModel();
        tablareporte.setColumnIdentifiers(new Object[]{"Producto", "Cantidad"});
        for (DTOReporte dTOReporte : reporte) 
        {
            tablareporte.addRow(new Object[] {dTOReporte.getProducto(), dTOReporte.getCantidad()});
            tblreporte.setModel(tablareporte);
        }       
    }
    
    private void llenarReporteFacturacion()
    {
        ProductoController gestor = new ProductoController();
        double facturacion = gestor.facturacionTotal();
        lblfacturacion.setText(String.valueOf(facturacion));
    }
    
    private void llenarReporteVenta()
    {
        ProductoController gestor = new ProductoController();
        DTOVenta venta = gestor.ventaMayorCantidadDeProductos();
        lblcantidad.setText(String.valueOf(venta.getCantidad()));
        lblprecio.setText(String.valueOf(venta.getPrecio()));
        lblcliente.setText(venta.getCliente());
        lblproducto.setText(venta.getProducto());
    }

