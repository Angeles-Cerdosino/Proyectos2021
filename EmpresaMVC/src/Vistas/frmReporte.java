    private void btnbuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnbuscarActionPerformed
        if(!txtimporte.getText().equals(""))
        {
            ProductoController gestor = new ProductoController();
            ArrayList<DTOVentaFiltro> filtro = gestor.VentasPorFiltroImporteTotal(Double.parseDouble(txtimporte.getText()));
            DefaultTableModel tablafiltro = new DefaultTableModel();
            tablafiltro.setColumnIdentifiers(new Object[]{"cliente", "producto", "importeTotal"});
            
            for (DTOVentaFiltro dTOVentaFiltro : filtro) 
            tablafiltro.addRow(new Object[] {dTOVentaFiltro.getCliente(), dTOVentaFiltro.getProducto(), dTOVentaFiltro.getImporteTotal()});
            tblreportefiltros.setModel(tablafiltro);            
        }
        else
            JOptionPane.showMessageDialog(this, "Ingrese un importe para filtrar!");
        
    }//GEN-LAST:event_btnbuscarActionPerformed

