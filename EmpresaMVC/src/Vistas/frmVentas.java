    private void btnaceptarventasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnaceptarventasActionPerformed
        String cliente = txtcliente.getText();
        int cantidad = Integer.parseInt(txtcantidad.getText());
        ProductoModel producto = (ProductoModel)cboproducto.getSelectedItem();
        
        Venta v = new Venta (0, cliente, cantidad, producto); //objeto venta
        ProductoController gestor = new ProductoController();
        gestor.agregarVenta(v);
        
        JOptionPane.showMessageDialog(this, "Se ha cargado producto con Ã©xito!");
    }//GEN-LAST:event_btnaceptarventasActionPerformed

