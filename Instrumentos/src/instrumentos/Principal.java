    public boolean validation()
    {
        if(txtnombrecliente.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(null, "El Nombre del Cliente debe agregarse");
            txtnombrecliente.requestFocus();
            return false;
        }
        
        if(txtnombreinstrumento.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(null, "El Nombre del Instrumento debe agregarse");
            txtnombreinstrumento.requestFocus();
            return false;
        }
        
        if(txtcantsemanas.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(null, "La cantidad de semanas debe agregarse");
            txtcantsemanas.requestFocus();
            return false;
        }
        if(rbtcuerda.isSelected())
        {
            if(txtcantcuerdas.getText().isEmpty())
            {
            JOptionPane.showMessageDialog(null, "La cantidad de cuerdas debe agregarse");
            txtcantcuerdas.requestFocus();
            return false;
            }                
        }       
        return true;
    }
            

