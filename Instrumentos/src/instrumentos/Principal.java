    private void btnaceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnaceptarActionPerformed
        if(validation()) 
        {
        String nombreCliente = txtnombrecliente.getText();
        String nombreInstrumento = txtnombreinstrumento.getText();
        int cantSemanas = Integer.parseInt(txtcantsemanas.getText());
        
        if(rbtviento.isSelected())
        {
            IViento iv = new IViento(nombreCliente, nombreInstrumento, cantSemanas, PROPERTIES);
            A.agregarAlquiler(iv);
        }
        else
        {
            int cantCuerdas = Integer.parseInt(txtcantcuerdas.getText());
            ICuerda ic = new ICuerda(nombreCliente, nombreInstrumento, cantSemanas, cantCuerdas);
            A.agregarAlquiler(ic);
        }
        
        lstclientes.setListData(A.getAlquileres().toArray()); //agrego el listado al list
        JOptionPane.showMessageDialog(this,"Alquiler registrado con éxito!");        
        txtnombrecliente.setText("");
        txtnombreinstrumento.setText("");
        txtcantcuerdas.setText("");
        txtcantsemanas.setText("");
        btnreportes.setEnabled(true);          
        }      
    }//GEN-LAST:event_btnaceptarActionPerformed

